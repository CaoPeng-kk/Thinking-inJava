package oct.test5;

public class Four_4 {

	public static void main(String[] args) {
		
		for(int x=1;x<=6;x++) {
			for(int y=1;y<x;y++) {
					System.out.print(" ");
				}
				for(int z=1;z<=6-x;z++) {
					System.out.print(z);
				}
				System.out.println("");

			}

		}
		
	
		
}


